#Operadores de Asignación [=, +=,-=,*=,/=,**=]

#Los operadores de asignación constan en dar una indicación a la variable correspondiente es decir, das una asingnación correspondiente a un valor de la variable asignada, te muestro

#Ejemplo simple 
num1 = 9 #-----> Por el simple hecho que tiene el simbolo de "=" <---- Igual hace referencia a num1 ---> igual a 9 es decir das una asignacion de un valor en concreto a tu variable,Perooooo que pasa si queremos hacer una suma es decir
valor1 = 7
#Lo casual sería
resultado = num1 + valor1 #Es verdad
print("Resultado 1:")
print(resultado) #Perooooo, para eso tenemos los operadores de asignación es decir

#Operador += <---- Sirve para sumar el valor al que queremos designar o sumar es decir:
print("Resultado 2:")
num1 += 7
print(num1)

#¿Dudas?No, ok seguimos

